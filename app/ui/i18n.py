def normalize_language(lang: str | None) -> str:
    if not lang:
        return "en"
    lowered = lang.lower().replace("_", "-")
    if lowered in {"pt", "pt-br"}:
        return "pt-BR"
    return "en" if lowered not in TRANSLATIONS else lang


def t(key: str, lang: str | None = None, **kwargs: Any) -> str:
    language = normalize_language(lang)
    table = TRANSLATIONS.get(language, TRANSLATIONS["en"])
    text = table.get(key, TRANSLATIONS["en"].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            return text
    return text


def get_translator(lang: str | None) -> Callable[[str], str]:
    language = normalize_language(lang)

    def _translator(key: str, **kwargs: Any) -> str:
        return t(key, language, **kwargs)

    return _translator
